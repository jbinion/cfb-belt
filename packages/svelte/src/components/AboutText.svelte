<div>
	<h2 class="sectionTitle mb-2">About the College Football Belt</h2>

	<div class="card border-[var(-accent)] bg-[var(--color-white)] p-6 shadow-md">
		<p class="mb-2 text-[var(--color-primary)]">
			The <span class="highlight">College Football Belt</span> is a fan-created championship system inspired
			by boxing's title belt concept. The idea is simple: the first-ever college football game established
			an original champion, and that "belt" has been defended in every game since, changing hands when
			the current holder loses.
		</p>
		<ul class="list-disc pl-6 text-[var(--color-primary)]">
			<li>The belt holder must defend their title in every game they play</li>
			<li>If the belt holder loses, the winner becomes the new champion</li>
			<li>The belt can change hands multiple times in a single season</li>
			<li>Any team can win the belt by defeating the current holder</li>
		</ul>
	</div>
